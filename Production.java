/**
 * The Production class represents the production resource in the game. It extends the
 * Resource class and provides production-specific functionality for a settlement.
 */
public class Production extends Resource {
	
	 /**
     * Constructs a Production resource with the specified amount.
     * 
     * @param amount is the quantity of production for the settlement
     */
	public Production(int amount) {
		super(amount);	
	}
}
